create or replace function wipe_room_data_instead_of_delete()
    returns trigger as
$$
    begin
        update rooms r
        set room_title = 'DELETED',
            room_description = 'DELETED',
            price = 1,
            can_extend = false,
            has_ac = false,
            has_hairdryer = false,
            has_wifi = false,
            has_tv = false,
            mountain_view = false,
            ocean_view = false
